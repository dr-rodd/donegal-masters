ALTER TABLE rounds DROP CONSTRAINT IF EXISTS uq_rounds_course;

UPDATE rounds
SET course_id = CASE id
  WHEN '22222222-0000-0000-0000-000000000001'::uuid THEN '11111111-0000-0000-0000-000000000002'::uuid
  WHEN '22222222-0000-0000-0000-000000000002'::uuid THEN '11111111-0000-0000-0000-000000000003'::uuid
  WHEN '22222222-0000-0000-0000-000000000003'::uuid THEN '11111111-0000-0000-0000-000000000001'::uuid
END
WHERE id IN (
  '22222222-0000-0000-0000-000000000001',
  '22222222-0000-0000-0000-000000000002',
  '22222222-0000-0000-0000-000000000003'
);

ALTER TABLE rounds ADD CONSTRAINT uq_rounds_course UNIQUE (course_id);
